package first_java;

public class Ifelse {

		public static void main(String [] args)
		{
			
			
			int n = 2;
			
			if(n==0)
				System.out.println("nothing");
			else if(n%2==0)
				System.out.println("Even");
			else
				System.out.println("Odd");
			 
			
			
			//ternary operator
			// ?:  ->condition?expr1:expr2
			
			
			int i = 8,j=0;
			j = (i>6)?1:2;
			
			System.out.println(j);
			
			
			
		
			}
			
		}
	


